<template>
  <div class="top-category">
    <div class="container m-top-20">
      <!-- 面包屑 -->
      <div class="bread-container">
        <el-breadcrumb separator=">">
          <el-breadcrumb-item :to="{ path: '/' }">首页</el-breadcrumb-item>
          <el-breadcrumb-item>{{ topCrumbs.name }}</el-breadcrumb-item>
        </el-breadcrumb>
      </div>
      <!-- 轮播图 -->
      <div class="home-banner">
        <el-carousel height="500px">
          <el-carousel-item v-for="item in bannerList" :key="item.id">
            <img :src="item.imgUrl" alt="" />
          </el-carousel-item>
        </el-carousel>
      </div>
      <!-- 分类列表展示 -->
      <div class="sub-list">
        <h3>全部分类</h3>
        <ul>
          <li v-for="i in topCrumbs.children" :key="i.id">
            <RouterLink to="/">
              <img :src="i.picture" />
              <p>{{ i.name }}</p>
            </RouterLink>
          </li>
        </ul>
      </div>
      <div class="ref-goods" v-for="item in topCrumbs.children" :key="item.id">
        <div class="head">
          <h3>- {{ item.name }}-</h3>
        </div>
        <div class="body">
          <GoodsItem v-for="good in item.goods" :goods="good" :key="good.id" />
        </div>
      </div>
    </div>
  </div>
</template>

<script setup>
import { ref, onMounted } from 'vue'
import { getTopCrumbsApi } from '@/apis/category.js'
import { getBannerApi } from '@/apis/home.js'
import { useRoute } from 'vue-router'
import GoodsItem from '@/components/GoodsItem.vue'
// 定义数据类型
const topCrumbs = ref({})
const bannerList = ref([])
// 路由获取id
const route = useRoute()

// 获取面包屑数据
const getTopCrumbs = async () => {
  // 传参 id
  const res = await getTopCrumbsApi(route.params.id)
  console.log(res)
  topCrumbs.value = res.result
}

const getBanner = async () => {
  const res = await getBannerApi({
    distributionSite: '2'
  })
  console.log(res)
  bannerList.value = res.result
}

onMounted(() => {
  getTopCrumbs()
  getBanner()
})
</script>

